            st.executeUpdate("insert into result values('"+rollNo+"','"+physicsMarks+"','"+mathMarks+"',+'"+chemMarks+"','"+electricalMarks+"','"+computerMarks+"')");
            JOptionPane.showMessageDialog(null, "Successfully Added");
